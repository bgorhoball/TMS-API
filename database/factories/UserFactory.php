<?php

namespace Database\Factories;

use App\Models\User;
use Illuminate\Database\Eloquent\Factories\Factory;
use Illuminate\Support\Str;

class UserFactory extends Factory
{
    /**
     * The name of the factory's corresponding model.
     *
     * @var string
     */
    protected $model = User::class;

    /**
     * Define the model's default state.
     *
     * @return array
     */
    public function definition()
    {
        return [
            User::FIELD_NAME => $this->faker->name,
            User::FIELD_EMAIL => $this->faker->unique()->safeEmail,
            User::FIELD_EMAIL_VERIFIED_AT => now(),
            User::FIELD_PASSWORD => '$2y$10$92IXUNpkjO0rOQ5byMi.Ye4oKoEa3Ro9llC/.og/at2.uheWG/igi', // password
            User::FIELD_REMEMBER_TOKEN => Str::random(10),
        ];
    }
}
